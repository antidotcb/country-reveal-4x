function countryCodeToFlag(code) {
    if (!code || typeof code !== 'string') return null;
    code = code.trim().toUpperCase();
    if (code.length !== 2) return null;
    const A = 0x1F1E6;
    const chars = [...code].map(c => String.fromCodePoint(A + c.charCodeAt(0) - 65));
    return chars.join('');
}

